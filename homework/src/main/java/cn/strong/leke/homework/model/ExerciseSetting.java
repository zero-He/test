package cn.strong.leke.homework.model;

import cn.strong.leke.model.BaseModel;

/**
 * 实体对象：练习设置表
 */
public class ExerciseSetting extends BaseModel {

	private static final long serialVersionUID = 1275963921982412915L;

	// 练习设置标识
	private Long settingId;
	// 练习类型
	private Integer exerciseType;
	// 学校标识
	private Long schoolId;
	// 年级标识
	private Long gradeId;
	// 年级名称
	private String gradeName;
	// 题目类型ID
	private Long questionTypeId;
	// 题目类型名称
	private String questionTypeName;
	//默认题目数量
	private Integer defaultNum;
	
	
	/**
	 * 练习设置标识
	 */
	public Long getSettingId() {
		return this.settingId;
	}

	/**
	 * 练习设置标识
	 */
	public void setSettingId(Long settingId) {
		this.settingId = settingId;
	}
	
	/**
	 * 练习类型
	 */
	public Integer getExerciseType() {
		return this.exerciseType;
	}

	/**
	 * 练习类型
	 */
	public void setExerciseType(Integer exerciseType) {
		this.exerciseType = exerciseType;
	}
	
	/**
	 * 学校标识。系统设置时学校ID=-1
	 */
	public Long getSchoolId() {
		return this.schoolId;
	}

	/**
	 * 学校标识。系统设置时学校ID=-1
	 */
	public void setSchoolId(Long schoolId) {
		this.schoolId = schoolId;
	}
	
	
	
	/**
	 * 题目类型ID
	 */
	public Long getQuestionTypeId() {
		return this.questionTypeId;
	}

	/**
	 * 题目类型ID
	 */
	public void setQuestionTypeId(Long questionTypeId) {
		this.questionTypeId = questionTypeId;
	}
	
	/**
	 * 题目类型名称
	 */
	public String getQuestionTypeName() {
		return this.questionTypeName;
	}

	/**
	 * 题目类型名称
	 */
	public void setQuestionTypeName(String questionTypeName) {
		this.questionTypeName = questionTypeName;
	}
	
	public Integer getDefaultNum() {
		return defaultNum;
	}

	public void setDefaultNum(Integer defaultNum) {
		this.defaultNum = defaultNum;
	}
	
	public Long getGradeId() {
		return gradeId;
	}

	public void setGradeId(Long gradeId) {
		this.gradeId = gradeId;
	}

	public String getGradeName() {
		return gradeName;
	}

	public void setGradeName(String gradeName) {
		this.gradeName = gradeName;
	}
}
