package cn.strong.leke.homework.model;

/**
 * 学生学科作业统计（待完成和待完成）
 * @author Zhang Fujun
 * @date 2016年6月24日
 */
public class SubjectHwStatistics {

	private Long subjectId;
	private String subjectName;
	private Long bugFixNum;
	private Long unfinishNum;

	/**
	 * @return the subjectId
	 */
	public Long getSubjectId() {
		return subjectId;
	}

	/**
	 * @param subjectId the subjectId to set
	 */
	public void setSubjectId(Long subjectId) {
		this.subjectId = subjectId;
	}

	/**
	 * @return the subjectName
	 */
	public String getSubjectName() {
		return subjectName;
	}

	/**
	 * @param subjectName the subjectName to set
	 */
	public void setSubjectName(String subjectName) {
		this.subjectName = subjectName;
	}

	/**
	 * @return the bugFixNum
	 */
	public Long getBugFixNum() {
		return bugFixNum;
	}

	/**
	 * @param bugFixNum the bugFixNum to set
	 */
	public void setBugFixNum(Long bugFixNum) {
		this.bugFixNum = bugFixNum;
	}

	/**
	 * @return the unfinishNum
	 */
	public Long getUnfinishNum() {
		return unfinishNum;
	}

	/**
	 * @param unfinishNum the unfinishNum to set
	 */
	public void setUnfinishNum(Long unfinishNum) {
		this.unfinishNum = unfinishNum;
	}


}
