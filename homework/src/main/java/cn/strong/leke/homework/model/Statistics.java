package cn.strong.leke.homework.model;

public class Statistics {
	private Integer total;
	private Integer doingTotal;
	private Integer bugfixTotal;
	public Integer getTotal() {
		return total;
	}
	public void setTotal(Integer total) {
		this.total = total;
	}
	public Integer getDoingTotal() {
		return doingTotal;
	}
	public void setDoingTotal(Integer doingTotal) {
		this.doingTotal = doingTotal;
	}
	public Integer getBugfixTotal() {
		return bugfixTotal;
	}
	public void setBugfixTotal(Integer bugfixTotal) {
		this.bugfixTotal = bugfixTotal;
	}
}
