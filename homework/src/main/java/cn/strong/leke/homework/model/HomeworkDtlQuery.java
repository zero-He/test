package cn.strong.leke.homework.model;

import java.util.Date;

/**
 * 描述:
 * @author DuanYanming
 * @created 2014年6月24日 下午4:40:46
 * @since v1.0.0
 */
public class HomeworkDtlQuery {

    // 学生ID
    private Long studentId;
    // 学校ID
    private Long schoolId;
    // 学科ID
    private Long subjectId;
    // 作业名称
    private String homeworkName;
    // 作业类型
    private Integer homeworkType;
    // 资源类型
    private Integer resType;
    // 作业分类，0：全部、1：未完成、2：已完成
    private Boolean isSubmit;
    // 批改状态，0：全部、1：未批发、2：已批改
    private Boolean isCorrect;
    //订正进度, 1:待订正,2:待复批,3:订正通过
    private Integer bugFixStage;
    // 截止时间开始
    private Date closeTime;
    // 截止时间结束
    private Date closeEndTime;

    private Integer start;

    private Integer limit;

    private Boolean isClose;

    //排序
    private String sort;

    public String getSort() {
        return sort;
    }

    public void setSort(String sort) {
        this.sort = sort;
    }

    public Long getStudentId() {
        return studentId;
    }

    public void setStudentId(Long studentId) {
        this.studentId = studentId;
    }

    public Long getSchoolId() {
        return schoolId;
    }

    public void setSchoolId(Long schoolId) {
        this.schoolId = schoolId;
    }

    public Long getSubjectId() {
        return subjectId;
    }

    public void setSubjectId(Long subjectId) {
        this.subjectId = subjectId;
    }

    public String getHomeworkName() {
        return homeworkName;
    }

    public void setHomeworkName(String homeworkName) {
        this.homeworkName = homeworkName;
    }

    public Integer getHomeworkType() {
        return homeworkType;
    }

    public void setHomeworkType(Integer homeworkType) {
        this.homeworkType = homeworkType;
    }

    public Integer getResType() {
        return resType;
    }

    public void setResType(Integer resType) {
        this.resType = resType;
    }

    public Date getCloseTime() {
        return closeTime;
    }

    public void setCloseTime(Date closeTime) {
        this.closeTime = closeTime;
    }

    public Date getCloseEndTime() {
        return closeEndTime;
    }

    public void setCloseEndTime(Date closeEndTime) {
        this.closeEndTime = closeEndTime;
    }

    public Integer getStart() {
        return start;
    }

    public void setStart(Integer start) {
        this.start = start;
    }

    public Integer getLimit() {
        return limit;
    }

    public void setLimit(Integer limit) {
        this.limit = limit;
    }

    public Boolean getIsSubmit() {
        return isSubmit;
    }

    public void setIsSubmit(Boolean isSubmit) {
        this.isSubmit = isSubmit;
    }

    public Boolean getIsCorrect() {
        return isCorrect;
    }

    public void setIsCorrect(Boolean isCorrect) {
        this.isCorrect = isCorrect;
    }

    /**
     * @return the bugFixStage
     */
    public Integer getBugFixStage() {
        return bugFixStage;
    }

    /**
     * @param bugFixStage the bugFixStage to set
     */
    public void setBugFixStage(Integer bugFixStage) {
        this.bugFixStage = bugFixStage;
    }

    public Boolean getIsClose() {
        return isClose;
    }

    public void setIsClose(Boolean isClose) {
        this.isClose = isClose;
    }


}
