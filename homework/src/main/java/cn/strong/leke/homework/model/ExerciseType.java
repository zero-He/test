package cn.strong.leke.homework.model;

/**
 * 练习类型枚举类。
 * @author andy
 */
public enum ExerciseType {

	/**
	 * 章节练习
	 */
	CHAPTER(1, "章节练习"),
	/**
	 * 知识点检测
	 */
	KNOWLEDGE(2, "知识点检测");

	/**
	 * 枚举值
	 */
	public final int value;
	/**
	 * 枚举名
	 */
	public final String name;

	private ExerciseType(int value, String name) {
		this.value = value;
		this.name = name;
	}
}
