package cn.strong.leke.homework.model.activities;

import java.util.Map;

public class MissionStatus {
	
	/**
	 * 任务key
	 */
	private String missionKey;
	/**
	 * 任务类型
	 */
	private String type;
	/**
	 * 任务状态 1进行中 2已经完成 3已经领取
	 */
	private Integer state;
	/**
	 * 奖励乐币
	 */
	private Integer lekeVal;
	
	/**
	 * 任务领取日期
	 */
	private Integer day;
	
	/**
	 * 数据
	 */
	private Map<String,Object> data;
	
	public String getMissionKey() {
		return missionKey;
	}

	public void setMissionKey(String missionKey) {
		this.missionKey = missionKey;
	}

	public Integer getLekeVal() {
		return lekeVal;
	}

	public void setLekeVal(Integer lekeVal) {
		this.lekeVal = lekeVal;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public Map<String, Object> getData() {
		return data;
	}

	public void setData(Map<String, Object> data) {
		this.data = data;
	}

	public Integer getDay() {
		return day;
	}

	public void setDay(Integer day) {
		this.day = day;
	}

	public Integer getState() {
		return state;
	}

	public void setState(Integer state) {
		this.state = state;
	}

}
