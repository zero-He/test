package cn.strong.leke.scs.model;

import java.math.BigDecimal;

import cn.strong.leke.model.BaseModel;

/**
 * 佣金累计表
 * @author  andy
 * @created 2015年6月15日 下午2:16:15
 * @since   v1.0.0
 */
public class Seller extends BaseModel {

	private static final long serialVersionUID = -2752790262208230995L;
	// 累计ID
	private Long accumId;
	// 销售ID
	private Long sellerId;
	// 销售姓名
	private String sellerName;
	// 客户数量
	private Integer customerNum;
	// 月度金额
	private BigDecimal monthAmount;
	// 年度金额
	private BigDecimal yearAmount;
	// 总金额
	private BigDecimal totalAmount;

	public Long getAccumId() {
		return accumId;
	}

	public void setAccumId(Long accumId) {
		this.accumId = accumId;
	}

	public Long getSellerId() {
		return sellerId;
	}

	public void setSellerId(Long sellerId) {
		this.sellerId = sellerId;
	}

	public String getSellerName() {
		return sellerName;
	}

	public void setSellerName(String sellerName) {
		this.sellerName = sellerName;
	}

	public Integer getCustomerNum() {
		return customerNum;
	}

	public void setCustomerNum(Integer customerNum) {
		this.customerNum = customerNum;
	}

	public BigDecimal getMonthAmount() {
		return monthAmount;
	}

	public void setMonthAmount(BigDecimal monthAmount) {
		this.monthAmount = monthAmount;
	}

	public BigDecimal getYearAmount() {
		return yearAmount;
	}

	public void setYearAmount(BigDecimal yearAmount) {
		this.yearAmount = yearAmount;
	}

	public BigDecimal getTotalAmount() {
		return totalAmount;
	}

	public void setTotalAmount(BigDecimal totalAmount) {
		this.totalAmount = totalAmount;
	}
}
