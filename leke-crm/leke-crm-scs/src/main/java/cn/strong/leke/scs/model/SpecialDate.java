package cn.strong.leke.scs.model;


import java.util.List;

import cn.strong.leke.model.BaseModel;

/**
 *
 * 描述:特殊节假日
 *
 * @author  zhengyiyin
 * @created 2015年11月24日 下午9:52:53
 * @since   v1.0.0
 */
public class SpecialDate extends BaseModel {
	

	/**
	 * Description: 
	 */
	private static final long serialVersionUID = 2777050322653236239L;

	/**
	 * Description: 特殊日ID
	 */
	private Long specialId;
	
	/**
	 * Description: 数字yyyyMMdd：20151111
	 */
	private String date;
	
	/**
	 * Description: 1=特殊节假日  2=特殊工作日
	 */
	private Integer type;
	
	private List<String> specialDayOff;
	
	private List<String> specialDayWork;

	public Long getSpecialId() {
		return specialId;
	}

	public void setSpecialId(Long specialId) {
		this.specialId = specialId;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public List<String> getSpecialDayOff() {
		return specialDayOff;
	}

	public void setSpecialDayOff(List<String> specialDayOff) {
		this.specialDayOff = specialDayOff;
	}

	public List<String> getSpecialDayWork() {
		return specialDayWork;
	}

	public void setSpecialDayWork(List<String> specialDayWork) {
		this.specialDayWork = specialDayWork;
	}

	
	
	
	
	
}
