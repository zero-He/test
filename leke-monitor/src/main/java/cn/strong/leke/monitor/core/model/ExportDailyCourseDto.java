public class ExportDailyCourseDto {

	private String day;// 日期
	private Long actualCount;// 实到人数
	private Long expectCount;// 应到人数
	private Long actualTimes;// 实到人次
	private Long expectTimes;// 应到人次
	private String ratioTimes;// 到课率(人次)

	public String getDay() {
		return day;
	}

	public void setDay(String day) {
		this.day = day;
	}

	public Long getActualCount() {
		return actualCount;
	}

	public void setActualCount(Long actualCount) {
		this.actualCount = actualCount;
	}

	public Long getExpectCount() {
		return expectCount;
	}

	public void setExpectCount(Long expectCount) {
		this.expectCount = expectCount;
	}

	public Long getActualTimes() {
		return actualTimes;
	}

	public void setActualTimes(Long actualTimes) {
		this.actualTimes = actualTimes;
	}

	public Long getExpectTimes() {
		return expectTimes;
	}

	public void setExpectTimes(Long expectTimes) {
		this.expectTimes = expectTimes;
	}

	public String getRatioTimes() {
		return ratioTimes;
	}

	public void setRatioTimes(String ratioTimes) {
		this.ratioTimes = ratioTimes;
	}
	

}
