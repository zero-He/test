package cn.strong.leke.monitor.core.model.online;

import cn.strong.leke.monitor.mongo.online.model.OnlineUserStat;

/**
 * 区域在线用户统计
 * 
 * @author liulongbiao
 *
 */
public class RegionOnlineUserStatDTO extends OnlineUserStat {
	private Long id; // 区域ID
	private String name; // 区域名称

	public RegionOnlineUserStatDTO() {
	}

	public RegionOnlineUserStatDTO(OnlineUserStat stat) {
		super(stat);
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
