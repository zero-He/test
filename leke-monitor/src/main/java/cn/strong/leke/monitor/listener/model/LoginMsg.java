package cn.strong.leke.monitor.listener.model;

/**
 * 登录消息
 * 
 * @author liulongbiao
 *
 */
public class LoginMsg {
	private String userId; // 用户ID
	private Long schoolId; // 用户当前学校ID
	private Long time; // 登录时间戳
	private String navigate; // 浏览器类型
	private String version; // 浏览器版本号
	private String os; // 用户操作系统
	private String userAgent; // 浏览器信息
	private String ip; // 用户IP

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public Long getSchoolId() {
		return schoolId;
	}

	public void setSchoolId(Long schoolId) {
		this.schoolId = schoolId;
	}

	public Long getTime() {
		return time;
	}

	public void setTime(Long time) {
		this.time = time;
	}

	public String getNavigate() {
		return navigate;
	}

	public void setNavigate(String navigate) {
		this.navigate = navigate;
	}

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	public String getOs() {
		return os;
	}

	public void setOs(String os) {
		this.os = os;
	}

	public String getUserAgent() {
		return userAgent;
	}

	public void setUserAgent(String userAgent) {
		this.userAgent = userAgent;
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

}
