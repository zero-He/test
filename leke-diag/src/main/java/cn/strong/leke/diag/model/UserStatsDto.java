package cn.strong.leke.diag.model;

public class UserStatsDto {

	private Long userId;
	private String userName;
	private Integer statNum;
	private Double submitRate;
	private Double delayRate;
	private Double undoneRate;
	private Double maxScore;
	private Double avgScore;

	public Long getUserId() {
		return userId;
	}

	public void setUserId(Long userId) {
		this.userId = userId;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public Integer getStatNum() {
		return statNum;
	}

	public void setStatNum(Integer statNum) {
		this.statNum = statNum;
	}

	public Double getSubmitRate() {
		return submitRate;
	}

	public void setSubmitRate(Double submitRate) {
		this.submitRate = submitRate;
	}

	public Double getDelayRate() {
		return delayRate;
	}

	public void setDelayRate(Double delayRate) {
		this.delayRate = delayRate;
	}

	public Double getUndoneRate() {
		return undoneRate;
	}

	public void setUndoneRate(Double undoneRate) {
		this.undoneRate = undoneRate;
	}

	public Double getMaxScore() {
		return maxScore;
	}

	public void setMaxScore(Double maxScore) {
		this.maxScore = maxScore;
	}

	public Double getAvgScore() {
		return avgScore;
	}

	public void setAvgScore(Double avgScore) {
		this.avgScore = avgScore;
	}
}
