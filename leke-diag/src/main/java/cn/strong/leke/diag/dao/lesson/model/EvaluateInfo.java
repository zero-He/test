package cn.strong.leke.diag.dao.lesson.model;

import java.util.List;

import cn.strong.leke.diag.model.NameValue;

public class EvaluateInfo {

	private Integer goodCount = 0; // 好评数
	private Integer userCount = 0; // 评价数
	private Double professionalScore = 0D; // 专业水平
	private Double rhythmScore = 0D; // 上课节奏
	private Double interactionScore = 0D; // 课堂互动
	private List<NameValue> trends; // 评价得分走势

	public Integer getGoodCount() {
		return goodCount;
	}

	public void setGoodCount(Integer goodCount) {
		this.goodCount = goodCount;
	}

	public Integer getUserCount() {
		return userCount;
	}

	public void setUserCount(Integer userCount) {
		this.userCount = userCount;
	}

	public Double getProfessionalScore() {
		return professionalScore;
	}

	public void setProfessionalScore(Double professionalScore) {
		this.professionalScore = professionalScore;
	}

	public Double getRhythmScore() {
		return rhythmScore;
	}

	public void setRhythmScore(Double rhythmScore) {
		this.rhythmScore = rhythmScore;
	}

	public Double getInteractionScore() {
		return interactionScore;
	}

	public void setInteractionScore(Double interactionScore) {
		this.interactionScore = interactionScore;
	}

	public List<NameValue> getTrends() {
		return trends;
	}

	public void setTrends(List<NameValue> trends) {
		this.trends = trends;
	}
}
