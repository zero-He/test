package cn.strong.leke.diag.dao.diag.model;

import java.io.Serializable;

public class ExamReport implements Serializable{
	
	private static final long serialVersionUID = 7506913150446689958L;
	
	//成绩单ID
	private Long examReportId;
	
	//成绩单名称
	private String examReportName;
	
	//成绩单文件保存路径
	private String filePath;
	
	//寄语
	private String greatings;
	
	//成绩单状态，1：待发布 2：已发布 3：作废
	private String status;
	
	private Long statusCd;
	
	//学校ID
	private Long schoolId;
	
	//学校名称
	private String schoolName;
	
	//创建人
	private Long createdBy;
	
	//创建人姓名
	private String createdUser;
	
	//创建日期
	private String createdOn;
	
	//发布日期
	private String modifiedOn;
	
	//是否自己创建，1：是，0：否
	private Integer isCreatedBySelf;
	
	private String studentId;

	public Long getExamReportId() {
		return examReportId;
	}

	public void setExamReportId(Long examReportId) {
		this.examReportId = examReportId;
	}

	public String getExamReportName() {
		return examReportName;
	}

	public void setExamReportName(String examReportName) {
		this.examReportName = examReportName;
	}
	
	public String getFilePath() {
		return filePath;
	}

	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}

	public String getGreatings() {
		return greatings;
	}

	public void setGreatings(String greatings) {
		this.greatings = greatings;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public Long getSchoolId() {
		return schoolId;
	}

	public void setSchoolId(Long schoolId) {
		this.schoolId = schoolId;
	}
	
	public String getSchoolName() {
		return schoolName;
	}

	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}

	public Long getCreatedBy() {
		return createdBy;
	}

	public void setCreatedBy(Long createdBy) {
		this.createdBy = createdBy;
	}

	public String getCreatedOn() {
		return createdOn;
	}

	public void setCreatedOn(String createdOn) {
		this.createdOn = createdOn;
	}

	public Integer getIsCreatedBySelf() {
		return isCreatedBySelf;
	}

	public void setIsCreatedBySelf(Integer isCreatedBySelf) {
		this.isCreatedBySelf = isCreatedBySelf;
	}

	public String getStudentId() {
		return studentId;
	}

	public void setStudentId(String studentId) {
		this.studentId = studentId;
	}

	public String getCreatedUser() {
		return createdUser;
	}

	public void setCreatedUser(String createdUser) {
		this.createdUser = createdUser;
	}

	public String getModifiedOn() {
		return modifiedOn;
	}

	public void setModifiedOn(String modifiedOn) {
		this.modifiedOn = modifiedOn;
	}

	public Long getStatusCd() {
		return statusCd;
	}

	public void setStatusCd(Long statusCd) {
		this.statusCd = statusCd;
	}
	
}
