package cn.strong.leke.diag.dao.lesson.model;

public class AttendStatInfo2 extends BaseAttend {

	private static final long serialVersionUID = -3062459066321878141L;

	private Integer lessonNum = 0; // 总课堂数
	private Integer realNum = 0; // 实上课堂数
	private Integer usersNum = 0; // 课堂人次

	public Integer getLessonNum() {
		return lessonNum;
	}

	public void setLessonNum(Integer lessonNum) {
		this.lessonNum = lessonNum;
	}

	public Integer getRealNum() {
		return realNum;
	}

	public void setRealNum(Integer realNum) {
		this.realNum = realNum;
	}

	public Integer getUsersNum() {
		return usersNum;
	}

	public void setUsersNum(Integer usersNum) {
		this.usersNum = usersNum;
	}
}