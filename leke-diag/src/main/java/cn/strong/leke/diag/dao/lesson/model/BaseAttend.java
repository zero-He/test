package cn.strong.leke.diag.dao.lesson.model;

public class BaseAttend implements java.io.Serializable {

	private static final long serialVersionUID = -7747653465222442249L;

	private Integer normalNum = 0; // 正常(全勤)
	private Integer belateNum = 0; // 迟到
	private Integer earlyNum = 0; // 早退
	private Integer exceptNum = 0; // 迟到且早退
	private Integer absentNum = 0; // 未到(缺勤)

	public Integer getNormalNum() {
		return normalNum;
	}

	public void setNormalNum(Integer normalNum) {
		this.normalNum = normalNum;
	}

	public Integer getBelateNum() {
		return belateNum;
	}

	public void setBelateNum(Integer belateNum) {
		this.belateNum = belateNum;
	}

	public Integer getEarlyNum() {
		return earlyNum;
	}

	public void setEarlyNum(Integer earlyNum) {
		this.earlyNum = earlyNum;
	}

	public Integer getExceptNum() {
		return exceptNum;
	}

	public void setExceptNum(Integer exceptNum) {
		this.exceptNum = exceptNum;
	}

	public Integer getAbsentNum() {
		return absentNum;
	}

	public void setAbsentNum(Integer absentNum) {
		this.absentNum = absentNum;
	}
}
