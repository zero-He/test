package cn.strong.leke.diag.model;

import java.math.BigDecimal;

public class ClassAnalysisDto {

	private Long classId;
	private String className;
	private BigDecimal rateA;
	private BigDecimal rateB;
	private BigDecimal rateC;
	private BigDecimal rateD;

	public Long getClassId() {
		return classId;
	}

	public void setClassId(Long classId) {
		this.classId = classId;
	}

	public String getClassName() {
		return className;
	}

	public void setClassName(String className) {
		this.className = className;
	}

	public BigDecimal getRateA() {
		return rateA;
	}

	public void setRateA(BigDecimal rateA) {
		this.rateA = rateA;
	}

	public BigDecimal getRateB() {
		return rateB;
	}

	public void setRateB(BigDecimal rateB) {
		this.rateB = rateB;
	}

	public BigDecimal getRateC() {
		return rateC;
	}

	public void setRateC(BigDecimal rateC) {
		this.rateC = rateC;
	}

	public BigDecimal getRateD() {
		return rateD;
	}

	public void setRateD(BigDecimal rateD) {
		this.rateD = rateD;
	}
}
