package cn.strong.leke.question.model;

import java.util.List;

import cn.strong.leke.model.BaseModel;

/**
 * 题目领取表
 * @author    lavender
 * @version   Avatar 
 */
public class QuestionTask extends BaseModel {
	/**
	 * serialVersionUID:
	 */
	private static final long serialVersionUID = 1L;
	private Long taskId;//领取id
	private Long userId;//用户id
	private List<Long> userIds;//用户ids
	private String userName;//用户名
	private Long gradeId;//年级id
	private String gradeName;//年级名
	private Long subjectId;//学科id
	private String subjectName;//学科名
	private Integer taskCount;//领取数量
	private Integer surplusCount;//剩余数量
	private Integer count;//数量
	
	public Long getTaskId() {
		return taskId;
	}
	public void setTaskId(Long taskId) {
		this.taskId = taskId;
	}
	public Long getUserId() {
		return userId;
	}
	public void setUserId(Long userId) {
		this.userId = userId;
	}


	/**
	 * @return the gradeId
	 */
	public Long getGradeId() {
		return gradeId;
	}
	
	/**
	 * @param gradeId the gradeId to set
	 */
	public void setGradeId(Long gradeId) {
		this.gradeId = gradeId;
	}
	
	/**
	 * @return the gradeName
	 */
	public String getGradeName() {
		return gradeName;
	}
	
	/**
	 * @param gradeName the gradeName to set
	 */
	public void setGradeName(String gradeName) {
		this.gradeName = gradeName;
	}
	public Long getSubjectId() {
		return subjectId;
	}
	public void setSubjectId(Long subjectId) {
		this.subjectId = subjectId;
	}
	
	public String getSubjectName() {
		return subjectName;
	}
	public void setSubjectName(String subjectName) {
		this.subjectName = subjectName;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public Integer getTaskCount() {
		return taskCount;
	}
	public void setTaskCount(Integer taskCount) {
		this.taskCount = taskCount;
	}
	public Integer getSurplusCount() {
		return surplusCount;
	}
	public void setSurplusCount(Integer surplusCount) {
		this.surplusCount = surplusCount;
	}
	public List<Long> getUserIds() {
		return userIds;
	}
	public void setUserIds(List<Long> userIds) {
		this.userIds = userIds;
	}

	/**
	 * @return the count
	 */
	public Integer getCount() {
		return count;
	}

	/**
	 * @param count the count to set
	 */
	public void setCount(Integer count) {
		this.count = count;
	}
}
