package cn.strong.leke.question.model;

/**
 * Word 导入记录错误明细
 * 
 * @author liulongbiao
 * @created 2014年12月18日 上午10:22:11
 * @since v3.2.1
 */
public class WordImportRecordError {

	private Long errorId; // 主键
	private String recordId; // 记录标识
	private Integer ord; // 习题序号
	private String error; // 错误消息
	private String summary; // 题干摘要
	private Boolean isDeleted; // 是否已删除

	public Long getErrorId() {
		return errorId;
	}

	public void setErrorId(Long errorId) {
		this.errorId = errorId;
	}

	public String getRecordId() {
		return recordId;
	}

	public void setRecordId(String recordId) {
		this.recordId = recordId;
	}

	public Integer getOrd() {
		return ord;
	}

	public void setOrd(Integer ord) {
		this.ord = ord;
	}

	public String getError() {
		return error;
	}

	public void setError(String error) {
		this.error = error;
	}

	public String getSummary() {
		return summary;
	}

	public void setSummary(String summary) {
		this.summary = summary;
	}

	public Boolean getIsDeleted() {
		return isDeleted;
	}

	public void setIsDeleted(Boolean isDeleted) {
		this.isDeleted = isDeleted;
	}

}
