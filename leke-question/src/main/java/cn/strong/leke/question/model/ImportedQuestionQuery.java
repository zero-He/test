package cn.strong.leke.question.model;

/**
 * 导入习题查询
 * 
 * @author liulongbiao
 * @created 2014年12月26日 下午4:06:18
 * @since v3.2.2
 */
public class ImportedQuestionQuery {

	private Long schoolStageId; // 学段ID
	private Long subjectId; // 学科ID
	private Long userId; // 用户ID
	private Integer status; // 习题状态

	public Long getSchoolStageId() {
		return schoolStageId;
	}

	public void setSchoolStageId(Long schoolStageId) {
		this.schoolStageId = schoolStageId;
	}

	public Long getSubjectId() {
		return subjectId;
	}

	public void setSubjectId(Long subjectId) {
		this.subjectId = subjectId;
	}

	public Long getUserId() {
		return userId;
	}

	public void setUserId(Long userId) {
		this.userId = userId;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

}
