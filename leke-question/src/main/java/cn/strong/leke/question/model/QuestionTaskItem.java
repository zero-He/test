package cn.strong.leke.question.model;

import cn.strong.leke.model.BaseModel;

/**
 * 题目领取详情实体
 * @author    lavender
 * @version   Avatar 
 */
public class QuestionTaskItem extends BaseModel {

	/**
	 * serialVersionUID:
	 */
	private static final long serialVersionUID = 1L;
	
	private Long userId;//用户id
	private Long taskItemId;//领取任务明细id
	private Long taskId;//领取id
	private Long questionId;//题目id
	
	public Long getTaskItemId() {
		return taskItemId;
	}
	public void setTaskItemId(Long taskItemId) {
		this.taskItemId = taskItemId;
	}
	public Long getTaskId() {
		return taskId;
	}
	public void setTaskId(Long taskId) {
		this.taskId = taskId;
	}
	public Long getQuestionId() {
		return questionId;
	}
	public void setQuestionId(Long questionId) {
		this.questionId = questionId;
	}
	public Long getUserId() {
		return userId;
	}
	public void setUserId(Long userId) {
		this.userId = userId;
	}
	
}
